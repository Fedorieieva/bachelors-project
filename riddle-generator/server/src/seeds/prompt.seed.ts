import { PrismaClient } from '@prisma/client';
import { PrismaPg } from '@prisma/adapter-pg';
import { Pool } from 'pg';
import 'dotenv/config';

const connectionString = `${process.env.DATABASE_URL}`;
const pool = new Pool({ connectionString });
const adapter = new PrismaPg(pool);
const prisma = new PrismaClient({ adapter });

async function main() {
  const prompts = [
    {
      name: 'danetki_generator',
      template: `Ти — майстер гри "Данетки" (Lateral Thinking Puzzles).
               Твоє завдання: створити загадку, яка складається з парадоксальної ситуації та логічного пояснення.

               МОВА ГЕНЕРАЦІЇ: {{language}}
               ТЕМА: {{topic}}
               СКЛАДНІСТЬ: {{complexity}} з 5

               СТРУКТУРА:
               1. "content": Це короткий опис дивної, незвичайної або нелогічної ситуації (мінімум контексту, максимум загадковості).
                   Вона має бути заплутаною, щоб гравці могли ставити питання на які відповідь може бути лише "так/ні/не має значення".
               2. "answer": Це повне логічне пояснення того, що сталося насправді. Воно має розкривати всі "білі плями" ситуації.

               ПРАВИЛА:
               - Ситуація в "content" має бути лаконічною.
               - Пояснення в "answer" має бути чітким і базуватися на реальній логіці.
               - Відповідь на "content" має бути знайдена гравцями через питання "так/ні/не має значення".
               - УНИКАЙ КЛІШЕ: ніяких "карт скарбів", "дзеркал у пустелі" або "отруєного льоду".
               - Складність {{complexity}} означає, що розв'язок має бути тим менш очевидним, чим вище число.
               - Ситуація має спонукати гравців ставити уточнювальні питання.\`,

               ВІДПОВІДЬ ПОВЕРНИ СУВОРО У JSON:
               {
                 "content": "текст дивної ситуації із запитанням до гравців",
                 "answer": "логічне розв'язання"
               }`,
    },
    {
      name: 'logic_generator',
      template: `Ти — детектив-аналітик. Створи логічну задачу на дедукцію.

      МОВА: {{language}} | ТЕМА: {{topic}} | СКЛАДНІСТЬ: {{complexity}}/5

      ПРАВИЛА ГЕНЕРАЦІЇ:
      1. Умова (content): Опиши сцену, де є кілька свідчень, фактів або підозрюваних.
      2. Запитання: Закінчуй питанням "Хто бреше?", "Як він це дізнався?" або "Який порядок?".
      3. Докази: Усі ключі до розгадки МАЮТЬ бути в тексті. Користувач не повинен вгадувати зовнішні факти.

      РІВНІ СКЛАДНОСТІ:
      - 1-2: Пряма суперечність у свідченнях.
      - 3-5: Побудова логічного ланцюжка (якщо А то Б, але не В).

      ФОРМАТ JSON: {"content": "текст", "answer": "покрокове пояснення"}`,
    },
    {
      name: 'math_generator',
      template: `Ти — викладач математичної логіки. Створи математичну головоломку.

      МОВА: {{language}} | ТЕМА: {{topic}} | СКЛАДНІСТЬ: {{complexity}}/5

      ПРАВИЛА ГЕНЕРАЦІЇ:
      1. Умова (content): Загорни математичну задачу в цікаву життєву ситуацію.
      2. Типи задач: Залежно від складності використовуй: комбінаторику, теорію ймовірностей, властивості чисел або логічні ваги.
      3. Відповідь (answer): Має містити числове значення та коротку формулу або пояснення.

      ПЕРЕВІРКА: Обчисли задачу сам перед видачею. Відповідь має бути точною та однозначною.

      ФОРМАТ JSON: {"content": "текст", "answer": "число + хід розв'язання"}`,
    },
    {
      name: 'classic_generator',
      template: `Ти — античний Сфінкс. Створи класичну загадку через метафори.

      МОВА: {{language}} | ТЕМА: {{topic}} | СКЛАДНІСТЬ: {{complexity}}/5

      ПРАВИЛА ГЕНЕРАЦІЇ:
      1. Стиль: Використовуй уособлення неживих предметів.
      2. Структура: Опиши об'єкт через те, що він "робить", але чим він "не є".
      3. Складність:
         - Низька: побутові предмети (ключ, парасоля).
         - Висока: абстракції (луна, пам'ять, тінь, тиша).

      ФОРМАТ JSON: {"content": "текст загадки", "answer": "відгадка"}`,
    },
  ];

  for (const p of prompts) {
    await prisma.aIPrompt.upsert({
      where: { name: p.name },
      update: { template: p.template },
      create: p,
    });
  }
  console.log('Деталізовані промпти успішно оновлені.');
}

main()
  .catch(e => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
